class Counter:
    def __init__(self, x):
        self.x = x
    def up(self):
        self.x += 1

    def down(self):
        self.x -= 1

    
object = Counter(0)
print(object.x)
object.up()
object.up()
print(object.x)
object.down()
print(object.x)
